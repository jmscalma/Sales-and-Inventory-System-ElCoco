    private void supplierMaintenanceTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_supplierMaintenanceTableMouseClicked
        updateBtn.setEnabled(false);
        
        supplierIdTf.setEnabled(false);
        supplierNameTf.setEnabled(false);
        contactPersonTf.setEnabled(false);
        addressTf.setEnabled(false);
        emailTf.setEnabled(false);
        contactNumberTf.setEnabled(false);
        statusSelect.setEnabled(false);

        int i = supplierMaintenanceTable.getSelectedRow();
        TableModel model = supplierMaintenanceTable.getModel();
        supplierIdTf.setText(model.getValueAt(i, 0).toString());
        supplierNameTf.setText(model.getValueAt(i, 1).toString());
        contactPersonTf.setText(model.getValueAt(i, 2).toString());
        addressTf.setText(model.getValueAt(i, 3).toString());
        emailTf.setText(model.getValueAt(i, 4).toString());
        contactNumberTf.setText(model.getValueAt(i, 5).toString());
        String status1 = model.getValueAt(i, 6).toString();
        switch(status1){
            case "Active":
            statusSelect.setSelectedIndex(0);
            break;
            
            case "Inactive":
            statusSelect.setSelectedIndex(1);
            break;
                    }
    }//GEN-LAST:event_supplierMaintenanceTableMouseClicked


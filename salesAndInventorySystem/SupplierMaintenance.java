     public void setRecordsToTable(){
        
        try
        {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/el_coco_db","root","root");
            PreparedStatement pst = con.prepareStatement("SELECT * FROM supplier_maintenance");
            ResultSet rs = pst.executeQuery();
            
            while(rs.next()){
                
                int supplier_id = rs.getInt("supplier_id");
                String supplierName = rs.getString("suppliername");
                String contactPerson = rs.getString("contactperson");
                String address = rs.getString("address");
                String email = rs.getString("email");
                String contactNumber = rs.getString("contactnumber");
                String status = rs.getString("status");
                
                Object[] obj = {supplier_id, supplierName, contactPerson, address, email, contactNumber, status };
                
                DefaultTableModel model = (DefaultTableModel)supplierMaintenanceTable.getModel();
                model.addRow(obj);
            }
            
        }catch(Exception e){
            e.printStackTrace();
        }
    }
    
    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        String supplierName = supplierNameTf.getText();
        String contactPerson = contactPersonTf.getText();
        String address = addressTf.getText();
        String email = emailTf.getText();
        String contactNumber = contactNumberTf.getText();
        String status = statusSelect.getSelectedItem().toString();
        

        if(supplierNameTf.getText().equals("") || contactPersonTf.getText().equals("") || addressTf.getText().equals("") || contactNumberTf.getText().equals("") || emailTf.getText().equals("") ){
            JOptionPane.showMessageDialog(this, "Please enter all data needed!");
        }else if(!contactNumber.matches("[0-9]+")){
            JOptionPane.showMessageDialog(this, "Incorrect Contact Number Input!", "Error", JOptionPane.ERROR_MESSAGE);
        }else if(!contactPerson.matches("^[ A-Za-z]+$")){
            JOptionPane.showMessageDialog(this, "Incorrect Name Input!", "Error", JOptionPane.ERROR_MESSAGE);
            
        }else{

            try{
                Statement s = DBConnector.mycon().createStatement();
                int row = supplierMaintenanceTable.getSelectedRow();
                String id = (supplierMaintenanceTable.getModel().getValueAt(row, 0).toString());
                String query = "UPDATE supplier_maintenance SET suppliername='"+supplierName+"', contactperson='"+contactPerson+"', address='"+address+"', email='"+email+"', contactnumber='"+contactNumber+"', status='"+status+"' where supplier_id =" + id;

                s.executeUpdate(query);

                DefaultTableModel model = (DefaultTableModel)supplierMaintenanceTable.getModel();
                model.setRowCount(0);

                JOptionPane.showMessageDialog(null, "Updated Successfully!");
                supplierMaintenanceTable.setModel(new DefaultTableModel(null, new String[]{"Supplier ID", "Supplier Name", "Contact Person", "Address", "E-mail", "Contact Number", "Status"}));
                setRecordsToTable();

                supplierNameTf.setEnabled(false);
                contactPersonTf.setEnabled(false);
                addressTf.setEnabled(false);
                contactNumberTf.setEnabled(false);
                emailTf.setEnabled(false);
                statusSelect.setEnabled(false);

            }catch(SQLException e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_updateBtnActionPerformed


    public void setRecordsToTable(){
        
        try
        {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/el_coco_db","root","root");
            PreparedStatement pst = con.prepareStatement("SELECT * FROM purchased_order");
            ResultSet rs = pst.executeQuery();
            
            while(rs.next()){
                
                int purchased_order_id = rs.getInt("purchased_order_id");
                int po_invoice_id = rs.getInt("po_invoice_id");
                int totalquantity = rs.getInt("totalquantity");
                int totalamount = rs.getInt("totalamount");
                String date = rs.getString("date");
                
                Object[] obj = {purchased_order_id, po_invoice_id, totalquantity, totalamount, date };
                
                javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel)purchasedOrderTransactionsTb.getModel();
                model.addRow(obj);
            }
            
        }catch(Exception e){
            e.printStackTrace();
        }
    }


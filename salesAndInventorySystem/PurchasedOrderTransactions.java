    private void allSalesBtn2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_allSalesBtn2ActionPerformed

        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String from = sdf.format(fromDate.getDate());
        String to = sdf.format(toDate.getDate());

        if(from.equals(" ") || to.equals(" ")){
            JOptionPane.showMessageDialog(this, "Select Dates!", "Error", JOptionPane.ERROR_MESSAGE);
        }else{

            try{
                String query = "SELECT * FROM purchased_order WHERE date >= '"+from+"' AND date <= '"+to+"' ";
                PreparedStatement pst = DBConnector.mycon().prepareStatement(query);

                ResultSet rs = pst.executeQuery(query);

                purchasedOrderTransactionsTb.setModel(DbUtils.resultSetToTableModel(rs));

            }catch(SQLException e){
                JOptionPane.showMessageDialog(this, e);
            }

            try{

                DefaultTableModel model = (DefaultTableModel) purchasedOrderTransactionsTb.getModel();
                JRTableModelDataSource ds = new JRTableModelDataSource(model);
                String r = "C:\\Users\\james\\Documents\\2y1 Activities\\OOP Projects\\ElCoco\\src\\reports\\BetweenDatesPurchasedOrderReport.jrxml";

                JasperReport jr = JasperCompileManager.compileReport(r);

                //get dates

                System.out.println(from);
                System.out.println(to);

                //get sum of amount due
                double total = 0.0;
                for (int ix = 0; ix < purchasedOrderTransactionsTb.getRowCount(); ix++){
                    double amount = Double.parseDouble(purchasedOrderTransactionsTb.getValueAt(ix, 3).toString());
                    total += amount;
                }
                System.out.println(total);
                //total quantity
                int totalq = 0;
                for (int ix = 0; ix < purchasedOrderTransactionsTb.getRowCount(); ix++){
                    double amount = Double.parseDouble(purchasedOrderTransactionsTb.getValueAt(ix, 2).toString());
                    totalq += amount;
                }

                HashMap<String, Object> p = new HashMap<String, Object>();
                p.put("FROM_DATE", from);
                p.put("TO_DATE", to);
                p.put("TOTAL_AMOUNT", total);
                p.put("TOTAL_QUANTITY", totalq);

                JasperPrint jp = JasperFillManager.fillReport(jr, p, ds);

                JasperViewer.viewReport(jp, false);

            }catch(Exception e){
                e.printStackTrace();
            }

        }

    }//GEN-LAST:event_allSalesBtn2ActionPerformed

